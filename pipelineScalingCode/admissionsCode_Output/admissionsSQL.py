file_path = r"C:\Users\prana\OneDrive - Georgia Institute of Technology\ResearchMIBLAB\ehr_summarization_BioMibLab\mimic-iv-demo\mimic-iv-clinical-database-demo-2.2\hosp\admissions\admissions.csv"
